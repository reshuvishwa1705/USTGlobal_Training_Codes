class Switch{
public static void main(String []args){
int marks=70;
switch(marks){
case 40:
System.out.println("Pass");
break;
case 50:
System.out.println("Second Class");
break;
case 60:
System.out.println("First Class Division");
break;
case 70:
System.out.println("First Class");
break;
default:
System.out.println("Not Matched");
}
}
}
